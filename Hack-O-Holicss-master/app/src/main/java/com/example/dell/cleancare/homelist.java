package com.example.dell.cleancare;

public class homelist {
    String name_home;
    String work_time;

    public homelist(String name_home, String work_time) {
        this.name_home = name_home;
        this.work_time = work_time;
    }

    public String getName_home() {
        return name_home;
    }

    public String getWork_time() {
        return work_time;
    }
}
